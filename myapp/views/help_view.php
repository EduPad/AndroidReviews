<h1 class="text-android">F.A.Q.</h1>

<hr>

<dl style="max-width:800px">

  <dt>Why Android Reviews Manager?</dt>

  <dd>
    <p>Because Google™ enables developers to reply to users’ reviews (which is great), but doesn’t provide a simple alert system to enable developers to be reactive (which is lame). Nor does (uber-fantastic) App Annie™ or any other service provider.</p>

    <p>We operate a company, eduPad, which runs160 Apps on Android, iOS, and Windows 8, and we used to have a person here working full time to monitor users’ reviews. So we needed something like Android Reviews Manager REALLY badly. So badly, actually, that one day we decided to build the service ourselves. Once we got it, we found it so good we had to share it with our peer Developers. Oh, and, BTW, the person who used to search the ADC for new users’ reviews several hours a day everyday now has time to focus on developing new exciting features for our Apps! ;)</p>

    <p>We believe that anyone serious about his Android Apps should use our service.</p>

  </dd>

  <dt>How does it work?</dt>

  <dd>
    <ol>
      <li>Search for the Apps you want to monitor. By default, Android Reviews Manager searches for Apps referenced in the US Play Store, but you can specify another country, based on the list of countries covered by Android Reviews Manager. This list will progressively increase in time.</li>

      <li>For every App you want to track, touch the “Start tracking” button. The last ten reviews will load and the App will be monitored daily from this moment on.</li>

      <li>Everyday, you’ll receive an email whenever a new user review is published on the Google Play Store on an App you monitor.</li>

      <li>Just like emails, you can “Mark as read” the reviews to dismiss them (they are still in the database, so you can “Mark them as unread” if needed.</li>

      <li>The “Reply” to a review button opens the Google Play Developer Console, where you can answer the user. Users just LOVE getting feedback from Developers, so we recommend that you answer even 5-star reviews, not only low-rated reviews.</li>

    </ol>

  </dd>

  <dt>I don’t see in Android Reviews Manager a review that I see in the Google Play Store</dt>

  <dd>
    <p>When you start tracking an App in Android Reviews Manager, only the last 10 reviews are logged. Afterwards, you see all the reviews posted by users unless your App gets more than 10 reviews in a given day. The frequency of reviews log and email alerts is something we’ll fine tune based upon your feedback during the beta. It is a tradeoff between catching all the reviews and not flooding you with emails).</p>
  </dd>

  <dt>Some reviews appear in the Google Play for Developer Console, but not in Android Reviews Manager</dt>

  <dd>
    <p>Indeed. There are 2 possible causes for that:</p>

    <ol>
      <li>The review comes from a country which is not yet covered by Android Reviews Manager. In this case, we do not have access yet to this review (yet).</li>

      <li>Since Google does not provide a public API for the Developer Console, Android Reviews Manager gets the reviews from the Google Play Store API. For an unknown reason, Google does not display all users’ reviews on the Play Store. The good news is: in Android Reviews Manager you get exactly the reviews the users see in the Google Play Store.</li>
    </ol>
  </dd>

  <dt>Will Android Reviews Manager remain free forever?</dt>

  <dd>
    <p>If Android Developers like our service, load and scale will be an issue, so we’ll have to charge a small monthly fee for the service.</p>

    <p>The Service will be free while in beta.</p>
  </dd>

  <dt>Why do you cover only the United States and France?</dt>

  <dd>
    <p>Because Google sends reviews based upon the country of your IP. So we had to set up a sophisticated VPN architecture to handle internationalization. In the beta, only 2 countries are supported: United States and France. A Geographic coverage extension will come progressively depending upon load and user feedback.</p>
  </dd>

